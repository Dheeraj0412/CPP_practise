/*write a program to find size of int, float,double and char.*/

#include<iostream>
using namespace std;

int main(){
    int a;
    float b;
    double c;
    char d;
    cout<<"int size: "<<sizeof(a)<<endl;
    cout<<"float size: "<<sizeof(b)<<endl;
    cout<<"double size: "<<sizeof(c)<<endl;
    cout<<"char size: "<<sizeof(d)<<endl;
    return 0;
}